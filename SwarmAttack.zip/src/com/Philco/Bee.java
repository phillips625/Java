public class Bee {

    private int healthPoint;
    private int damages;

    public Bee(int healthPoint, int damages) {
        this.healthPoint = healthPoint;
        this.damages = damages;
    }

    public int getHealthPoint() {
        return healthPoint;
    }

    public int getDamages() {
        return damages;
    }
}


